package com.bignerdranch.android.blackboard.Blackboard.LatestMessage;

public class MessageItem {

    private String mName;
    private int mPhoto;
    private int mText;
    private int mStar;

    public String getmName() {
        return mName;
    }
    public void setmName(String mName) {
        this.mName = mName;
    }

    public int getmPhoto() {
        return mPhoto;
    }
    public void setmPhoto(int mPhoto) {
        this.mPhoto = mPhoto;
    }

    public int getmText() {
        return mText;
    }
    public void setmText(int mText) {
        this.mText = mText;
    }

    public int ismStar() {
        return mStar;
    }
    public void setmStar(int mStar) {
        this.mStar = mStar;
    }


}
